public class LinkedList {
    private Actor head;
    private int count;

    public LinkedList() {
        head = null;
        count = 0;
    }

    public void add(Actor imdb) {
        Actor oldHead = head;
        head = imdb;
        head.setNext(oldHead);
        count++;
    }

    public Actor get(int index) {
        Actor dummy = head;
        for (int i = 0; i < count; i++) {
            if (i == index) {
                return dummy;
            }
            dummy = dummy.getNext();
        }
        return null;
    }

    public int size() {
        return count;
    }
}
